void main(List<String> args) {
  // Operadores Relacionais
  // == (igualdade), !=(diferença), > (maior), < (menor), <= (menor igual), >= (maior igual)

  int idade = 30;
  if (idade == 30) {
    print('Idade igual a 30');
  }
  if (idade != 20) {
    print('Idade diferente de 20');
  }
  if (idade > 17) {
    print('É maior de idade');
  }
  if (idade < 18) {
    print('Menor de idade');
  }
}
